class Random:
    pattern: list[list]

    def generate(self, rows: int, columns: int) -> list[list]:
        assert rows <= 75
        assert columns <= 75
        pattern = []
        for ri in range(rows):
            row = []
            for ci in range(columns):
                row.append(random.getrandbits(1))
            pattern.append(row)

        self.pattern = pattern
        return self.pattern
